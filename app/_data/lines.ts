export type Station = {
  code: string;
  name: string;
};

export type Line = {
  code: string;
  name: string;
  operator: string;
  color: string;
  stations: Station[];
};

type StationDef = [string, string];

function makeLine(
  code: string,
  name: string,
  operator: string,
  color: string,
  defs: StationDef[]
): Line {
  return {
    code,
    name,
    operator,
    color,
    stations: defs.map(([number, stationName]) => ({
      code: `${code}${number}`,
      name: stationName,
    })),
  };
}

