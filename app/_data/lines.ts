export const STATION_COUNT = LINES.reduce((sum, line) => sum + line.stations.length, 0);

export type OperatorGroup = {
  operator: string;
  lines: Line[];
};

export const LINES_BY_OPERATOR: OperatorGroup[] = (() => {
  const groups: OperatorGroup[] = [];
  for (const line of LINES) {
    let group = groups.find((g) => g.operator === line.operator);
    if (!group) {
      group = { operator: line.operator, lines: [] };
      groups.push(group);
    }
    group.lines.push(line);
  }
  return groups;
})();

export const LINE_BY_CODE: Record<string, Line> = Object.fromEntries(
  LINES.map((line) => [line.code, line])
);

export function findStation(lineCode: string, stationCode: string): Station | undefined {
  return LINE_BY_CODE[lineCode]?.stations.find((s) => s.code === stationCode);
}
